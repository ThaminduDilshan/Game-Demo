    public void display(GridLocation grd, JLabel lbl){
        String s = "";
        
        if( grd.get() instanceof LotusFlower ){
            s = s + "LF";
        }else if( grd.get() instanceof TreasureChest ){
            s = s + "TC";
        }else if( grd.get() instanceof InnocentFish ){
            s = s + "IF";
        }else if( grd.get() instanceof EatRubFish ){
            s = s + "ERF";
        }else if( grd.get() instanceof KillerFish ){
            s = s + "KF";
        }
        if( grd.getWar() instanceof NormalWarrior ){
            s = s + " NW";
        }else if( grd.getWar() instanceof SuperWarrior ){
            s = s + " SW";
        }
        
        lbl.setText(s);
    }
    
